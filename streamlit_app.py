import numpy as np
import pickle
import pandas as pd
import streamlit as st 

from PIL import Image

pickle_in = open("HousePredictionFile","rb")
classifier=pickle.load(pickle_in)
def welcome():
    return "Welcome All"
def predict_note_authentication(lotsize,bathrms,stories,driveway,recroom,fullbase,gashw,airco,garagepl): 
    prediction=classifier.predict([[lotsize,bathrms,stories,driveway,recroom,fullbase,gashw,airco,garagepl]])
    print(prediction)
    return prediction



def main():
    st.title("House price Prediction")
    html_temp = """
    <div style="background-color:tomato;padding:10px">
    <h2 style="color:white;text-align:center;">Streamlit Bank Authenticator ML App </h2>
    </div>
    """
    st.markdown(html_temp,unsafe_allow_html=True)
    lotsize = st.text_input("lotsize","Type Here")
    bathrms = st.text_input("bathrms","Type Here")
    stories = st.text_input("stories","Type Here")
    driveway = st.text_input("driveway","Type Here")
    recroom = st.text_input("recroom","Type Here")
    fullbase = st.text_input("fullbase","Type Here")
    gashw = st.text_input("gashw","Type Here")
    airco = st.text_input("airco","Type Here")
    garagepl = st.text_input("garagepl","Type Here")
    result=""
    if st.button("Predict"):
        result=predict_note_authentication(lotsize,bathrms,stories,driveway,recroom,fullbase,gashw,airco,garagepl)
    st.success('The output is {}'.format(result))
    if st.button("About"):
        st.text("Lets Learn")
        st.text("Built with Streamlit")

if __name__=='__main__':
    main()
